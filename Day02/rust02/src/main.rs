fn main() {
    println!("Variables type");

    let a = 100;
    let b = 3.141592;

    println!("int   : {}", a);
    println!("float : {}", b);

    // 타입 지정
    let c: i16 = 12345;
    let d: f64 = 12.45726;
    let e: bool = false;

    println!("int   : {}", c);
    println!("float : {}", d);
    println!("bool  : {}", e);

}
